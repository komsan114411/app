// mobile/scripts/apply-hardening.js â€” After `npx cap sync android`,
// copy hardening resources into the Capacitor-generated android project
// and enable minifyEnabled in app/build.gradle.

const fs = require('node:fs');
const path = require('node:path');

const here   = __dirname;
const mobile = path.join(here, '..');
const res    = path.join(mobile, 'android-resources');
const app    = path.join(mobile, 'android', 'app');

if (!fs.existsSync(app)) {
  console.error('âś– mobile/android/app not found â€” run `npx cap add android` first');
  process.exit(1);
}

function copyInto(srcFile, destDir, destName) {
  const dest = path.join(destDir, destName || path.basename(srcFile));
  fs.mkdirSync(destDir, { recursive: true });
  fs.copyFileSync(srcFile, dest);
  console.log('  âś“ copied', path.relative(mobile, dest));
}

// 1) Network security config + backup rules into res/xml/
copyInto(path.join(res, 'network_security_config.xml'), path.join(app, 'src', 'main', 'res', 'xml'));
copyInto(path.join(res, 'backup_rules.xml'),            path.join(app, 'src', 'main', 'res', 'xml'));

// 2) Append proguard rules (don't overwrite â€” Capacitor may add its own)
const proguardSrc  = path.join(res, 'proguard-rules.pro');
const proguardDest = path.join(app, 'proguard-rules.pro');
if (fs.existsSync(proguardDest)) {
  const existing = fs.readFileSync(proguardDest, 'utf8');
  const addon    = fs.readFileSync(proguardSrc, 'utf8');
  if (!existing.includes('# â”€â”€ Keep Capacitor core')) {
    fs.writeFileSync(proguardDest, existing + '\n\n' + addon);
    console.log('  âś“ appended proguard-rules.pro');
  } else {
    console.log('  Â· proguard-rules.pro already hardened');
  }
} else {
  copyInto(proguardSrc, app);
}

// 3) Patch AndroidManifest to add hardening attributes. Uses a simple
//    attribute injection into the <application> tag. Safe because the
//    manifest is always regenerated by `cap sync`.
const manifestPath = path.join(app, 'src', 'main', 'AndroidManifest.xml');
if (fs.existsSync(manifestPath)) {
  let xml = fs.readFileSync(manifestPath, 'utf8');
  const attrs = {
    'android:allowBackup':              '"false"',
    'android:fullBackupContent':        '"@xml/backup_rules"',
    'android:networkSecurityConfig':    '"@xml/network_security_config"',
    'android:usesCleartextTraffic':     '"false"',
    'android:extractNativeLibs':        '"false"',
    'android:requestLegacyExternalStorage': '"false"',
  };
  // Ensure tools xmlns exists
  if (!xml.includes('xmlns:tools=')) {
    xml = xml.replace('<manifest ', '<manifest xmlns:tools="http://schemas.android.com/tools" ');
  }
  // POST_NOTIFICATIONS is REQUIRED on Android 13+ (API 33+). Without
  // it declared here, the runtime permission dialog triggered by
  // `Notification.requestPermission()` inside the WebView is silently
  // denied â€” the user sees a brief "Allow" prompt that seemingly
  // succeeds, but `Notification.permission` stays `'default'` and
  // `pushManager.subscribe()` is called anyway â†’ the subscription
  // endpoint is stored, the backend can send, the browser's push
  // service accepts the POST, BUT the OS-level permission check
  // at display time drops every notification on the floor.
  //
  // VIBRATE + WAKE_LOCK let the notification vibrate the phone and
  // wake the screen / WebView when a push arrives in the background.
  const requiredPerms = [
    'android.permission.POST_NOTIFICATIONS',
    'android.permission.VIBRATE',
    'android.permission.WAKE_LOCK',
  ];
  for (const p of requiredPerms) {
    if (!xml.includes(`android:name="${p}"`)) {
      xml = xml.replace(
        '<application',
        `<uses-permission android:name="${p}"/>\n    <application`,
      );
    }
  }
  // Tools replace list so our attributes override Capacitor's defaults
  const toolsReplace = Object.keys(attrs).join(',');
  if (!xml.includes('tools:replace=')) {
    xml = xml.replace('<application', `<application tools:replace="${toolsReplace}"`);
  }
  // Inject each attribute idempotently
  for (const [key, val] of Object.entries(attrs)) {
    if (xml.match(new RegExp('\\b' + key.replace(':', '\\:') + '='))) {
      xml = xml.replace(new RegExp(key.replace(':', '\\:') + '="[^"]*"'), `${key}=${val}`);
    } else {
      xml = xml.replace('<application', `<application\n        ${key}=${val}`);
    }
  }
  fs.writeFileSync(manifestPath, xml);
  console.log('  âś“ hardened AndroidManifest.xml');
}

// 4) Enable minifyEnabled + shrinkResources in release build.gradle
const gradlePath = path.join(app, 'build.gradle');
if (fs.existsSync(gradlePath)) {
  let gradle = fs.readFileSync(gradlePath, 'utf8');
  if (/minifyEnabled\s+true/.test(gradle)) {
    console.log('  Â· build.gradle already has minifyEnabled');
  } else {
    gradle = gradle.replace(/release\s*\{([^}]*)\}/, (block, inner) => {
      const added = `\n            minifyEnabled true\n            shrinkResources true\n            proguardFiles getDefaultProguardFile('proguard-android-optimize.txt'), 'proguard-rules.pro'\n`;
      return 'release {\n' + inner + added + '        }';
    });
    fs.writeFileSync(gradlePath, gradle);
    console.log('  âś“ enabled R8 + resource shrinking in release');
  }
}

console.log('hardening applied âś“');
